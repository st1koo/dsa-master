struct Node {
	int data;
	Node* left;
	Node* right;
};

// utility fxn
Node* newNode(int n) {
	Node* temp = new Node;
	temp->data = n;
	temp->left = temp->right = NULL;
	return(temp);
}

int maxSum(Node* root) {
	if(root == NULL) return 0;
	int left = maxSum(root->left);
	int right = maxSum(root->right);
	return max(left,right) + root->data;
}

